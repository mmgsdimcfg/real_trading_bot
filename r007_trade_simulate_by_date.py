class TradeRecord:
    def __init__(
        self,
        code: str,
        name: str,
        action: str,
        bar_time: pd.Timestamp,
        price: float,
        qty: int,
        reason: str,
        session: str,
    ):
        self.code = code
        self.name = name
        self.action = action
        self.bar_time = bar_time
        self.price = price
        self.qty = qty
        self.reason = reason
        self.session = session
        self.pnl_pct: float | None = None
        self.pnl_krw: float | None = None

    def __repr__(self) -> str:
        pnl_str = f" | pnl={self.pnl_pct:.2f}%" if self.pnl_pct is not None else ""
        return (
            f"{self.bar_time:%H:%M} | {self.action:4s} | {self.code}({self.name}) | "
            f"qty={self.qty} | price={self.price:,.0f}{pnl_str} | {self.reason}"
        )


class SimPosition:
    def __init__(self, code: str, name: str, buy_price: float, quantity: int, buy_time: pd.Timestamp, buy_session: str):
        self.code = code
        self.name = name
        self.buy_price = buy_price
        self.quantity = quantity
        self.buy_time = buy_time
        self.buy_session = buy_session
        self.highest_price = buy_price


class Simulator:
    def __init__(self, initial_capital: float):
        self.initial_capital = initial_capital
        self.cash = initial_capital
        self.positions: dict[str, SimPosition] = {}
        self.trade_log: list[TradeRecord] = []
        self.cooldown_until: dict[str, pd.Timestamp] = {}
        self.completed_codes: set[str] = set()

    def in_cooldown(self, code: str, now: pd.Timestamp) -> bool:
        until = self.cooldown_until.get(code)
        return until is not None and now < until

    def set_cooldown(self, code: str, now: pd.Timestamp) -> None:
        self.cooldown_until[code] = now + pd.Timedelta(minutes=TRADE_COOLDOWN_MINUTES)

    def buy(self, code: str, name: str, price: float, now: pd.Timestamp, session: str, reason: str) -> bool:
        if self.in_cooldown(code, now) or price <= 0 or code in self.positions:
            return False
        if (not ALLOW_REBUY_SAME_CODE) and (not SIM_ALLOW_REENTRY_AFTER_COMPLETED_SELL) and code in self.completed_codes:
            return False

        qty = int(MAX_ORDER_AMOUNT_KRW / price)
        if qty <= 0:
            return False

        affordable_qty = int(self.cash / price)
        qty = min(qty, affordable_qty)
        if qty <= 0:
            return False

        self.cash -= price * qty
        self.positions[code] = SimPosition(code, name, price, qty, now, session)
        self.set_cooldown(code, now)

        rec = TradeRecord(code, name, "BUY", now, price, qty, reason, session)
        self.trade_log.append(rec)
        log(f"  {rec}")
        return True

    def sell(self, code: str, price: float, now: pd.Timestamp, reason: str, session: str) -> bool:
        pos = self.positions.get(code)
        if pos is None or pos.quantity <= 0 or price <= 0:
            return False

        proceeds = price * pos.quantity
        self.cash += proceeds

        rec = TradeRecord(code, pos.name, "SELL", now, price, pos.quantity, reason, session)
        rec.pnl_pct = (price / pos.buy_price - 1.0) * 100.0
        rec.pnl_krw = (price - pos.buy_price) * pos.quantity
        self.trade_log.append(rec)
        log(f"  {rec}")

        del self.positions[code]
        self.completed_codes.add(code)
        self.set_cooldown(code, now)
        return True

    def portfolio_value(self, last_prices: dict[str, float]) -> float:
        total = self.cash
        for code, pos in self.positions.items():
            total += last_prices.get(code, pos.buy_price) * pos.quantity
        return total


def get_latest_price_up_to(frame: pd.DataFrame, ts: pd.Timestamp) -> float | None:
    available = frame[frame.index <= ts]
    if available.empty:
        return None
    return float(available.iloc[-1]["close"])


def _format_pending_pnl(reason_prefix: str, pnl_pct: float) -> str:
    return f"{reason_prefix}_{pnl_pct * 100:.2f}%"


def _session_exit_plan(reason_prefix: str, pnl_pct: float) -> tuple[str, str]:
    if pnl_pct > 0:
        return "sell", _format_pending_pnl(f"{reason_prefix}_PROFIT_CLOSE", pnl_pct)
    if pnl_pct >= STOP_LOSS_PERCENT:
        return "hold", _format_pending_pnl(f"{reason_prefix}_OVERNIGHT_HOLD_WITHIN_STOP", pnl_pct)
    return "sell", _format_pending_pnl(f"{reason_prefix}_STOP_LOSS_BREACH", pnl_pct)


def run_scheduled_liquidations(
    sim: Simulator,
    price_frames: dict[str, pd.DataFrame],
    names: dict[str, str],
    ts: pd.Timestamp,
    state: dict,
    nxt_tradeable_fn,
) -> None:
    current_date = ts.date()
    if state.get("date") != current_date:
        state["date"] = current_date
        state["done_regular"] = False
        state["done_nxt"] = False

    if not state["done_regular"] and ts.time() >= REGULAR_FORCE_EXIT:
        state["done_regular"] = True
        for code in list(sim.positions.keys()):
            pos = sim.positions.get(code)
            if pos is None:
                continue
            price = get_latest_price_up_to(price_frames[code], ts)
            if price is None:
                continue
            pnl_pct = (price / pos.buy_price) - 1.0 if pos.buy_price > 0 else 0.0
            action, reason = _session_exit_plan("REGULAR_CLOSE", pnl_pct)
            if action == "sell":
                sim.sell(code, price, ts, reason, "regular")

    if ENABLE_NXT_SESSION and (not state["done_nxt"]) and ts.time() >= AFTERNOON_NXT_FORCE_EXIT:
        state["done_nxt"] = True
        for code in list(sim.positions.keys()):
            pos = sim.positions.get(code)
            if pos is None:
                continue
            if not bool(nxt_tradeable_fn(code)):
                continue
            price = get_latest_price_up_to(price_frames[code], ts)
            if price is None:
                continue
            pnl_pct = (price / pos.buy_price) - 1.0 if pos.buy_price > 0 else 0.0
            action, reason = _session_exit_plan("NXT_CLOSE", pnl_pct)
            if action == "sell":
                sim.sell(code, price, ts, reason, "afternoon_nxt")


def _load_nxt_flags(data_dir: Path) -> dict[str, bool]:
    """r001_data_collect_symbols_daily.py가 저장한 nxt_flags.json을 로드한다.

    당일분은 dict 형태로 읽어오며, 없으면 fallback 로직을 사용한다.
    """
    path = data_dir / "nxt_flags.json"
    if not path.exists():
        return {}
    try:
        with open(path, encoding="utf-8") as f:
            raw = json.load(f)
        return {str(code).zfill(6): bool(flag) for code, flag in raw.items()}
    except Exception as exc:
        log(f"WARNING: failed to load nxt_flags.json: {exc}")
        return {}



