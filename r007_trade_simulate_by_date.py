                    no_trend_exit_state.pop(code, None)
                    sim.sell(code, price, ts, reason_breakeven, session)
                    signal_sell_bar[code] = ts
                    log(f"  [SELL EXECUTED] {code} | {reason_breakeven} | price={price:,.0f}")
