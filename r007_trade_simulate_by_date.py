def check_buy_condition_r76_sim(
    frame: pd.DataFrame,
    ts: pd.Timestamp,
    live_price: float,
    cross_info: dict[str, object],
    intrabar_elapsed_seconds: float | None = None,
) -> tuple[bool, str]:
    ok, reason = shared_check_buy_condition(
        frame=frame,
        now=ts,
        live_price=live_price,
        cross_info=cross_info,
        config=SHARED_R76_CONFIG,
        volume_ratio_threshold_fn=get_volume_ratio_threshold,
    )

    if not ok:
        return ok, reason

    if ENABLE_INTRABAR_LIVE_ENTRY_FILTER and len(frame) >= 2:
        cur = frame.iloc[-1]
        prev = frame.iloc[-2]
        if intrabar_elapsed_seconds is not None and intrabar_elapsed_seconds < INTRABAR_MIN_ELAPSED_SECONDS:
            return False, f"INTRABAR_TOO_EARLY_{intrabar_elapsed_seconds:.0f}s_LT_{INTRABAR_MIN_ELAPSED_SECONDS:.0f}s"

        ema20_cur = _num(cur, "EMA_20")
        ema5_cur = _num(cur, "EMA_5")
        vwap_cur = _num(cur, "VWAP")
        ema20_prev = _num(prev, "EMA_20")
        close_prev = _num(prev, "close")
        if any(pd.isna(v) for v in (ema20_cur, vwap_cur)) or not (live_price > ema20_cur and live_price > vwap_cur):
            return False, f"INTRABAR_LIVE_NOT_ABOVE_EMA20_VWAP_{live_price:.0f}"
        if any(pd.isna(v) for v in (close_prev, ema20_prev)) or not (close_prev < ema20_prev):
            return False, f"INTRABAR_PREV_CLOSE_NOT_BELOW_EMA20_{close_prev:.1f}_GE_{ema20_prev:.1f}"
        if any(pd.isna(v) for v in (ema5_cur, ema20_cur)) or not (ema5_cur > ema20_cur):
            return False, f"INTRABAR_EMA_ALIGNMENT_FAIL_EMA5_{ema5_cur:.1f}_LE_EMA20_{ema20_cur:.1f}"

        mfi_prev = _num(prev, "MFI")
        rsi_prev = _num(prev, "RSI")
        adx_prev = _num(prev, "ADX")
        di_plus_prev = _num(prev, "DI_PLUS")
        di_minus_prev = _num(prev, "DI_MINUS")
        if pd.isna(mfi_prev) or not (INTRABAR_MFI_MIN <= mfi_prev < INTRABAR_MFI_MAX):
            return False, f"INTRABAR_MFI_RANGE_FAIL_{mfi_prev:.1f}"
        if pd.isna(rsi_prev) or not (INTRABAR_RSI_MIN <= rsi_prev < INTRABAR_RSI_MAX):
            return False, f"INTRABAR_RSI_RANGE_FAIL_{rsi_prev:.1f}"
        if pd.isna(adx_prev) or adx_prev < INTRABAR_ADX_MIN:
            return False, f"INTRABAR_ADX_TOO_LOW_{adx_prev:.1f}_LT_{INTRABAR_ADX_MIN:.1f}"
        if pd.isna(di_plus_prev) or pd.isna(di_minus_prev) or di_plus_prev <= di_minus_prev:
            return False, f"INTRABAR_DI_NOT_BULLISH_+DI_{di_plus_prev:.1f}_-DI_{di_minus_prev:.1f}"

    return ok, reason


def collect_buy_reject_reasons_r76_sim(
    frame: pd.DataFrame,
    ts: pd.Timestamp,
    live_price: float,
    cross_info: dict[str, object],
    primary_reason: str,
) -> list[str]:
    reasons: list[str] = []
    if primary_reason:
        reasons.append(primary_reason)

    if len(frame) < 2:
        return list(dict.fromkeys(reasons))

    cur = frame.iloc[-1]
    prev = frame.iloc[-2]
    cur_bb = _num(cur, "BB_MIDDLE")
    cur_bb_upper = _num(cur, "BB_UPPER")
    prev_bb = _num(prev, "BB_MIDDLE")
    if any(pd.isna(v) for v in (cur_bb, cur_bb_upper, prev_bb)):
        if "MISSING_INDICATOR" not in reasons:
            reasons.append("MISSING_INDICATOR")
        return list(dict.fromkeys(reasons))

    # BB slope
    bb_slope_pct = _compute_bb_slope_pct(frame)
    if pd.isna(bb_slope_pct) or bb_slope_pct <= 0.0:
        slope_str = f"{bb_slope_pct:.3f}" if not pd.isna(bb_slope_pct) else "nan"
        reasons.append(f"BB_SLOPE_NOT_RISING_{slope_str}%")

    # BB mid cross up
    live_cross_up = cross_info.get("signal") == "cross_up"
    prev_close = _num(prev, "close")
    cur_close = _num(cur, "close")
    close_cross = (
        not any(pd.isna(v) for v in (prev_close, cur_close, prev_bb, cur_bb))
        and prev_close <= prev_bb
        and cur_close > cur_bb
    )
    if not live_cross_up and not close_cross:
        reasons.append("NO_BB_MID_CROSS_UP")

    # Current candle bullish
    cur_open = _num(cur, "open")
    if not pd.isna(cur_open) and cur_open > 0:
        candle_gain_pct = (live_price - cur_open) / cur_open * 100.0
        if candle_gain_pct <= CANDLE_GAIN_MIN_PCT:
            reasons.append(f"CANDLE_NOT_BULLISH_{candle_gain_pct:.2f}%")
    else:
        reasons.append("CANDLE_OPEN_MISSING")

    # BB upper gap
    if live_price > 0 and not pd.isna(cur_bb_upper):
        bb_upper_gap_pct = (cur_bb_upper - live_price) / live_price * 100.0
        if bb_upper_gap_pct < BB_UPPER_GAP_MIN_PCT:
            reasons.append(f"BB_UPPER_GAP_TOO_SMALL_{bb_upper_gap_pct:.2f}%")

    # Score
    score = _buy_support_score(cur, prev, frame=frame)
    if score < SHARED_R76_CONFIG.bb_buy_score_threshold:
        reasons.append(f"LOW_SCORE_{score}_LT_{SHARED_R76_CONFIG.bb_buy_score_threshold}")

    return list(dict.fromkeys(reasons))


