@dataclass
class PaperStats:
    trades: int = 0
    wins: int = 0
    losses: int = 0
    pnl_sum_pct: float = 0.0
    equity: float = 1.0


class PaperStrategyTracker:
    def __init__(self, name: str, mode: str):
        self.name = name
        self.mode = mode  # basic / multi
        self.positions: dict[str, dict] = {}
        self.traded_today: set[str] = set()
        self.last_bar_seen: dict[str, pd.Timestamp] = {}
        self.stats = PaperStats()
        # mode="multi" tracks per-code live-price/BB cross state (same as main Simulator)
        self.cross_state: dict[str, dict] = {}

    def _on_close(self, code: str, price: float, reason: str) -> None:
        pos = self.positions.pop(code, None)
        if pos is None:
            return
        entry = float(pos["entry_exec_price"])
        if entry <= 0:
            return
        exit_exec = float(price) * (1.0 - COMPARE_SLIPPAGE_RATE - COMPARE_FEE_SELL_RATE)
        pnl = (exit_exec / entry) - 1.0
        self.stats.trades += 1
        self.stats.pnl_sum_pct += pnl
        self.stats.equity *= max(0.0, 1.0 + pnl)
        if pnl > 0:
            self.stats.wins += 1
        else:
            self.stats.losses += 1
        log_detail(
            f"[COMPARE-{self.name}] SELL | {code} | {reason} | "
            f"raw_exit={price:,.0f} exec_exit={exit_exec:,.0f} pnl={pnl*100:.2f}%"
        )

    def on_bar(self, code: str, frame: pd.DataFrame, ts: pd.Timestamp, entry_allowed: bool, live_price: float | None = None) -> None:
        if frame is None or frame.empty:
            return

        price = float(frame.iloc[-1]["close"])
        _lp = live_price if live_price is not None else price

        # mode="multi": update cross_state on EVERY tick (not gated by bar dedup)
        # Matches main Simulator which updates sim_live_cross_state each 20s tick.
        _multi_cross_info: dict[str, object] = {"signal": None, "relation": "on", "upper_trigger": 0.0, "lower_trigger": 0.0}
        if self.mode == "multi":
            _cur_bar_for_cross = frame.iloc[-1]
            _bb_mid = _num(_cur_bar_for_cross, "BB_MIDDLE")
            if not pd.isna(_bb_mid):
                _multi_cross_info = _update_sim_cross_state(self.cross_state, code, ts, _lp, _bb_mid)

        bar_time = frame.index[-1]
        if self.last_bar_seen.get(code) == bar_time:
            return
        self.last_bar_seen[code] = bar_time

        if code in self.positions:
            if self.mode == "basic":
                sell_ok, reason = check_sell_condition_basic(frame)
            else:
                pos = self.positions[code]
                entry = float(pos["entry_price"])
                pnl_pct = (price / entry) - 1.0
                pos["highest_price"] = max(float(pos.get("highest_price", price)), price)
                highest = float(pos.get("highest_price", price))
                if pnl_pct <= STOP_LOSS_PERCENT:
                    sell_ok, reason = True, f"STOP_LOSS_{STOP_LOSS_PERCENT*100:.1f}%"
                else:
                    peak_pnl = (highest / entry) - 1.0 if entry > 0 else 0.0
                    giveback = peak_pnl - pnl_pct
                    if ENABLE_TP_EXTENSION_TRAILING and peak_pnl >= TAKE_PROFIT_PERCENT:
                        trail = TP_EXTENSION_TRAIL_FROM_PEAK
                        ts_reason = f"TP_EXTENSION_TRAIL_{TP_EXTENSION_TRAIL_FROM_PEAK*100:.1f}%"
                    else:
                        trail = TRAILING_STOP_FROM_PEAK
                        ts_reason = f"TRAILING_STOP_GIVEBACK_{TRAILING_STOP_FROM_PEAK*100:.1f}%"
                    if peak_pnl > 0 and pnl_pct > 0 and giveback >= trail:
                        sell_ok, reason = True, ts_reason
                    else:
                        held_seconds = (ts - pos["buy_time"]).total_seconds() if isinstance(pos.get("buy_time"), pd.Timestamp) else None
                        sell_ok, reason = check_sell_condition(frame, pnl_pct, held_seconds)

            if sell_ok:
                self._on_close(code, price, reason)
            return

        if not entry_allowed:
            return
        if (not ALLOW_REBUY_SAME_CODE) and (not SIM_ALLOW_REENTRY_AFTER_COMPLETED_SELL) and code in self.traded_today:
            return

        if self.mode == "basic":
            buy_ok, reason = check_buy_condition_basic(frame)
        else:
            # Use shared live-price/BB cross logic (same as r006 live trading)
            buy_ok, reason = check_buy_condition_r76_sim(frame, ts, _lp, _multi_cross_info)

        if not buy_ok:
            return

        entry_exec = float(price) * (1.0 + COMPARE_SLIPPAGE_RATE + COMPARE_FEE_BUY_RATE)
        self.positions[code] = {
            "entry_price": float(price),
            "entry_exec_price": float(entry_exec),
            "highest_price": float(price),
            "buy_time": ts,
        }
        self.traded_today.add(code)
        log_detail(
            f"[COMPARE-{self.name}] BUY  | {code} | {reason} | "
            f"raw_entry={price:,.0f} exec_entry={entry_exec:,.0f}"
        )

    def force_close_all(self, price_map: dict[str, float], reason: str) -> None:
        for code in list(self.positions.keys()):
            price = float(price_map.get(code) or self.positions[code].get("entry_price") or 0.0)
            if price > 0:
                self._on_close(code, price, reason)

    def summary_dict(self) -> dict[str, float | int]:
        trades = self.stats.trades
        win_rate = (self.stats.wins / trades * 100.0) if trades else 0.0
        avg_pnl = (self.stats.pnl_sum_pct / trades * 100.0) if trades else 0.0
        total_pnl = self.stats.pnl_sum_pct * 100.0
        return {
            "trades": trades,
            "wins": self.stats.wins,
            "losses": self.stats.losses,
            "win_rate": win_rate,
            "avg_pnl_pct": avg_pnl,
            "total_pnl_pct": total_pnl,
            "equity": self.stats.equity,
        }


