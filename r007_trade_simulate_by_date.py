def _code_name_label(code: str, name: str | None) -> str:
    name_text = str(name or "").strip()
    if not name_text or name_text == str(code):
        return str(code)
    return f"{code}_{name_text}"


def _load_text_lines(path: Path) -> list[str]:
    for encoding in ("utf-8-sig", "utf-8", "cp949"):
        try:
            with open(path, "r", encoding=encoding) as file_obj:
                return [line.strip() for line in file_obj if line.strip()]
        except UnicodeDecodeError:
            continue
    raise UnicodeDecodeError("define_today_code", b"", 0, 1, "Unable to decode file")


def load_code_name_map(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}

    result: dict[str, str] = {}
    for line in _load_text_lines(path):
        if line.startswith("#"):
            continue
        parts = [part.strip() for part in line.split(",")]
        # Backward compatible formats:
        # - code,name
        # - YYYYMMDD,code,name
        if len(parts) >= 3 and parts[0].isdigit() and len(parts[0]) == 8:
            code = parts[1].zfill(6)
            name = parts[2] if parts[2] else code
        else:
            code = parts[0].zfill(6)
            name = parts[1] if len(parts) >= 2 and parts[1] else code
        result[code] = name
    return result


