def _simulate_relaxed_shared_gate(
    frame: pd.DataFrame,
    live_price: float,
    cross_info: dict[str, object],
    shared_reason: str,
) -> tuple[bool, str]:
    """Replay-only fallback for shared entry gates that can lock simulation to zero trades."""
    if len(frame) < 2:
        return False, shared_reason

    cur = frame.iloc[-1]
    prev = frame.iloc[-2]
    cur_bb = _num(cur, "BB_MIDDLE")
    prev_bb = _num(prev, "BB_MIDDLE")
    cur_ma5 = _num(cur, "MA_5")
    prev_ma5 = _num(prev, "MA_5")
    if any(pd.isna(v) for v in (cur_bb, prev_bb, cur_ma5, prev_ma5)):
        return False, shared_reason

    # Keep trend-shape guards even in relaxed mode.
    if (not SIM_RELAXED_ALLOW_BELOW_BB) and live_price <= cur_bb:
        return False, "RELAXED_REJECT_LIVE_PRICE_NOT_ABOVE_BB_MIDDLE"
    if (not SIM_RELAXED_ALLOW_FALLING_TREND) and cur_bb < prev_bb:
        return False, "RELAXED_REJECT_BB_MIDDLE_FALLING"
    if (not SIM_RELAXED_ALLOW_FALLING_TREND) and cur_ma5 < prev_ma5:
        return False, "RELAXED_REJECT_MA5_FALLING"

    support_score = _buy_support_score(cur, prev, frame=frame)
    score_ok = support_score >= float(SIM_RELAXED_MIN_SUPPORT_SCORE)
    confirmed_above = cross_info.get("confirmed_relation") == "above"
    ma5_bias_ok = cur_ma5 >= cur_bb and cur_ma5 >= prev_ma5

    if shared_reason in {"NO_LIVE_PRICE_BB_CROSS_UP", "LIVE_NOT_ABOVE_PREV_CLOSE_AND_BB_MIDDLE"}:
        if SIM_RELAXED_REQUIRE_CONFIRMED_ABOVE and not confirmed_above:
            return False, "RELAXED_REJECT_CROSS_NOT_CONFIRMED"
        if not score_ok:
            return False, f"RELAXED_REJECT_LOW_SUPPORT_{support_score}"
        if SIM_RELAXED_REQUIRE_MA5_BIAS and not ma5_bias_ok:
            return False, "RELAXED_REJECT_MA5_BIAS_FAIL"
        return True, f"RELAXED_CROSS_GATE_BYPASS_SCORE_{support_score}"

    if shared_reason == "NO_VWAP_BREAK":
        vwap = _num(cur, "VWAP")
        close_v = _num(cur, "close")
        if pd.isna(vwap) or pd.isna(close_v) or vwap <= 0:
            if score_ok:
                return True, f"RELAXED_VWAP_NAN_BYPASS_SCORE_{support_score}"
            return False, "RELAXED_REJECT_VWAP_NAN"

        under_pct = (vwap - close_v) / vwap
        if under_pct <= float(SIM_RELAXED_VWAP_MAX_UNDER_PCT) and score_ok:
            return True, f"RELAXED_VWAP_SOFT_BYPASS_{under_pct*100:.2f}%_SCORE_{support_score}"
        return False, f"RELAXED_REJECT_VWAP_TOO_LOW_{under_pct*100:.2f}%"

    return False, shared_reason


def _price_lead_breakout_context_sim(
    cur: pd.Series,
    prev: pd.Series,
    frame: pd.DataFrame,
    ts: pd.Timestamp,
    price: float,
    cross_info: dict,
    nxt_tradeable: bool,
) -> dict:
    """r006 _price_lead_breakout_context() 시뮬레이션 버전."""
    result = {"can_enter": False, "mode": "", "reason": ""}

    bb_upper = _num(cur, "BB_UPPER")
    price_breakout = price > bb_upper and not pd.isna(bb_upper)
    live_cross_up = cross_info.get("live_cross_up", False)

    if not price_breakout and not live_cross_up:
        result["reason"] = "NO_PRICE_LEAD_SIGNAL"
        return result

    vol_col = "volume" if "volume" in cur.index else "VOLUME"
    vol = _num(cur, vol_col) if vol_col in cur.index else float("nan")
    vol_ma = _num(cur, "VOLUME_MA") if "VOLUME_MA" in cur.index else float("nan")
    if not pd.isna(vol) and not pd.isna(vol_ma) and vol_ma > 0:
        if vol < vol_ma * 0.5:
            result["reason"] = "PRICE_LEAD_LOW_LIQUIDITY"
            return result

    adx = _num(cur, "ADX")
    di_plus = _num(cur, "DI_PLUS")
    di_minus = _num(cur, "DI_MINUS")
    macd = _num(cur, "MACD")
    macd_signal = _num(cur, "MACD_SIGNAL")
    macd_hist = _num(cur, "MACD_HIST")

    adx_ok = not pd.isna(adx) and adx >= PRICE_LEAD_BREAKOUT_MIN_ADX
    momentum_ok = (
        not any(pd.isna(v) for v in (macd, macd_signal, macd_hist))
        and macd > macd_signal
        and macd_hist > 0
    )

    near_cross = cross_info.get("near_cross", False)
    near_cross_armed = cross_info.get("near_cross_armed", False)

    if price_breakout and adx_ok and momentum_ok:
        result["can_enter"] = True
        result["mode"] = "PRICE_LEAD_BREAKOUT"
        result["reason"] = f"PRICE_LEAD_BREAKOUT_ADX{adx:.0f}_MACD{macd_hist:.3f}"
        return result

    if live_cross_up and adx_ok and momentum_ok:
        result["can_enter"] = True
        result["mode"] = "PRICE_LEAD_CROSS_UP"
        result["reason"] = f"PRICE_LEAD_CROSS_UP_ADX{adx:.0f}_MACD{macd_hist:.3f}"
        return result

    if near_cross_armed and adx_ok:
        result["can_enter"] = True
        result["mode"] = "ARMED_NEAR_CROSS"
        result["reason"] = f"ARMED_NEAR_CROSS_ADX{adx:.0f}"
        return result

    if near_cross and adx_ok:
        result["can_enter"] = True
        result["mode"] = "EARLY_NEAR_CROSS"
        result["reason"] = f"EARLY_NEAR_CROSS_ADX{adx:.0f}"
        return result

    result["reason"] = "PRICE_LEAD_CONDITIONS_NOT_MET"
    return result


def _extract_aux_score_from_reason(reason: str) -> int | None:
    """AUX sell reason string에서 score 값 추출. e.g. 'AUX_SCORE3_...' -> 3"""
    import re
    m = re.search(r"AUX_SCORE(\d+)", reason)
    return int(m.group(1)) if m else None


