def is_nxt_session(ts: pd.Timestamp) -> bool:
    if not ENABLE_NXT_SESSION:
        return False
    current_time = ts.time()
    return (MORNING_NXT_START <= current_time <= MORNING_NXT_END) or (AFTERNOON_NXT_START <= current_time <= AFTERNOON_NXT_END)


